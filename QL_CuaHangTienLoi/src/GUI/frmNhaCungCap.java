    private void updateTable()
    {
        String []TieuDe = {"Mã nhà cung cấp", "Tên nhà cung cấp", "Số điện thoại", "Địa chỉ"};
        ArrayList<clsNhaCungCap> dsNCC = NhaCungCapDAO.layDanhSachNhaCungCap();
        DefaultTableModel dtm = (DefaultTableModel)tblNhaCungCap.getModel();
        dtm.setColumnIdentifiers(TieuDe);
        
        dtm.setRowCount(0);
        for(clsNhaCungCap hh : dsNCC)
        {
            Vector<Object> vec = new Vector<Object>();
            vec.add(hh.getMaNCC());
            vec.add(hh.getTenNCC());
            vec.add(hh.getsDT());
            vec.add(hh.getDiaChi());
            dtm.addRow(vec);
        }
    }
