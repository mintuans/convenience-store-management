    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int maNCC = Integer.parseInt(txtMaNCC.getText());
        int confirm = JOptionPane.showConfirmDialog(this,
            "Bạn có chắc chắn muốn xóa nhân viên có mã " + maNCC + " không?",
            "Xác nhận xóa",
            JOptionPane.YES_NO_OPTION,
            JOptionPane.QUESTION_MESSAGE);
        boolean result = NhaCungCapDAO.xoaNhaCungCap(maNCC);
        if (confirm == JOptionPane.YES_OPTION) {
            if(result)
            {
                updateTable();
                Reset();
                JOptionPane.showMessageDialog(this, "Xóa thành công mã nhà cung cấp " + maNCC);

            }else
            {
                JOptionPane.showMessageDialog(this, "Xóa không thành công mã nhà cung cấp " + maNCC);
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

