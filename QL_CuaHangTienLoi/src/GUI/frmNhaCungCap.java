    private void tblNhaCungCapMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNhaCungCapMouseClicked
        // TODO add your handling code here:
        int rowIndex = tblNhaCungCap.getSelectedRow();

        if (rowIndex >= 0) {
            int maNCC = (int) tblNhaCungCap.getValueAt(rowIndex, 0);

            clsNhaCungCap ncc = NhaCungCapDAO.layNhaCungCap(maNCC);

            txtMaNCC.setText(String.valueOf(ncc.getMaNCC()));
            txtTenNCC.setText(ncc.getTenNCC());

            txtSDT.setText(ncc.getsDT());
            txtDiaChi.setText(ncc.getDiaChi());
        }
    }//GEN-LAST:event_tblNhaCungCapMouseClicked

