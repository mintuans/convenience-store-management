    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int maHH = Integer.parseInt(txtMaLoai.getText());

        boolean result = HangHoaDAO.xoaLoaiHangHoa(maHH);

        if(result)
        {
            load_theloai();
            JOptionPane.showMessageDialog(this, "Xóa thành công mã loại hàng " + maHH);

        }else
        {
            JOptionPane.showMessageDialog(this, "Xóa không thành công mã loại hàng " + maHH);
        }

    }//GEN-LAST:event_btnXoaActionPerformed

