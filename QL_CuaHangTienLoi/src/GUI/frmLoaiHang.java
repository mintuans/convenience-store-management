    public void load_theloai() {
        ArrayList<clsLoaiHang> ds = HangHoaDAO.layLoaiHang();
        DefaultComboBoxModel<clsLoaiHang> model = new DefaultComboBoxModel<>();

        for (clsLoaiHang x : ds) {
            model.addElement(x); 
        }

        cboMaLoai.setModel(model); 
        cboMaLoai.setSelectedIndex(0); 
    }
