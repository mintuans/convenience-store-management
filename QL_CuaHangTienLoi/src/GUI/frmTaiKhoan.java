    private void updateTable()
    {
        String []TieuDe = {"Tên tài khoản", "Mật khẩu", "Loại tài khoản", "Mã nhân viên"};
        ArrayList<clsTaiKhoan> dsNCC = DangNhapDAO.layDanhSachTaiKhoan();
        DefaultTableModel dtm = (DefaultTableModel)tblTaiKhoan.getModel();
        dtm.setColumnIdentifiers(TieuDe);
        
        dtm.setRowCount(0);
        for(clsTaiKhoan hh : dsNCC)
        {
            Vector<Object> vec = new Vector<Object>();
            vec.add(hh.getTenTaiKhoan());
            vec.add(hh.getMatKhau());
            vec.add(hh.getLoaiTaiKhoan());
            vec.add(hh.getMaNhanVien());
            dtm.addRow(vec);
        }
    }
