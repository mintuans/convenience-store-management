    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int maNCC = Integer.parseInt(txtMaNV.getText());
        int confirm = JOptionPane.showConfirmDialog(this,
            "Bạn có chắc chắn muốn xóa nhân viên có mã " + maNCC + " không?",
            "Xác nhận xóa",
            JOptionPane.YES_NO_OPTION,
            JOptionPane.QUESTION_MESSAGE);
        boolean result = DangNhapDAO.xoaTaiKhoan(maNCC);
        if (confirm == JOptionPane.YES_OPTION) {
            if(result)
            {
                updateTable();
                Reset();
                JOptionPane.showMessageDialog(this, "Xóa thành công tài khoản có mã nhân viên " + maNCC);

            }else
            {
                JOptionPane.showMessageDialog(this, "Xóa không thành công tài khoản có mã nhân viên " + maNCC);
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

